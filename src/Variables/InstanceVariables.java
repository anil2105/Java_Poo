package Variables;

public class InstanceVariables 
{
	byte b;

	int accBal;
	
	char c;

	double d;

	boolean b1;

	String s1;
	
	public static void main(String[] args)
	{
		
		InstanceVariables  o1=new InstanceVariables (); //Object creation
	
		System.out.println(o1.b);
		System.out.println(o1.accBal);
		System.out.println(o1.c);
		System.out.println(o1.d);
		System.out.println(o1.b1);
		System.out.println(o1.s1);
	
	}	
	
}

