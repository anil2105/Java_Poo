package Operators;

public class Test1 
{
	public static void main(String[] args) 
	{
		//Assignment operators  [+= -= *= /= %=]
		
		int a=10;
		
		int b=2;
		
		int c=4;
		
		int d=8;
		
		int e=20;
		
		a+=30;
		a+=50;
		b-=2;
		c*=10;
		c*=2;
		d/=2;
		e%=5;
		
		System.out.println(a); //90
		
		System.out.println(b); //0
		
		System.out.println(c); //80
		
		System.out.println(d); //4
		
		System.out.println(e); //0
		
	}
}
